import { Sequelize, Op } from 'sequelize';

import { NODE_ENV } from '../constants';
import { POSTGRES_MAX_CONNECTIONS, POSTGRES_MAX_RETRIES, POSTGRES_MIN_CONNECTIONS } from '../constants/postgres';

const configDb = require('../config/database');

const config = configDb[NODE_ENV];

const operatorsAliases = {
  $eq: Op.eq,
  $ne: Op.ne,
  $gte: Op.gte,
  $gt: Op.gt,
  $lte: Op.lte,
  $lt: Op.lt,
  $not: Op.not,
  $in: Op.in,
  $notIn: Op.notIn,
  $is: Op.is,
  $like: Op.like,
  $notLike: Op.notLike,
  $iLike: Op.iLike,
  $notILike: Op.notILike,
  $regexp: Op.regexp,
  $notRegexp: Op.notRegexp,
  $iRegexp: Op.iRegexp,
  $notIRegexp: Op.notIRegexp,
  $between: Op.between,
  $notBetween: Op.notBetween,
  $overlap: Op.overlap,
  $contains: Op.contains,
  $contained: Op.contained,
  $adjacent: Op.adjacent,
  $strictLeft: Op.strictLeft,
  $strictRight: Op.strictRight,
  $noExtendRight: Op.noExtendRight,
  $noExtendLeft: Op.noExtendLeft,
  $and: Op.and,
  $or: Op.or,
  $any: Op.any,
  $all: Op.all,
  $values: Op.values,
  $col: Op.col,
};

const sequelize = new Sequelize(config.database, config.username, config.password, {
  host: config.host,
  port: config.port,
  dialect: config.dialect,
  pool: {
    max: POSTGRES_MAX_CONNECTIONS,
    min: POSTGRES_MIN_CONNECTIONS,
    acquire: 30000,
    idle: 10000,
  },
  retry: {
    max: POSTGRES_MAX_RETRIES,
  },
  logging: false,
  operatorsAliases,
});

export default sequelize;
